	//---------------------------------------------
	//時間計測
	void Object::CountTime() {
		if (this->IsTimeZero() == false) {
			//時間計測
			float newtime = (float)timeGetTime();
			timeCnt -= (newtime - this->oldTime) / 1000.0f;
			oldTime = newtime;
		}
	}
